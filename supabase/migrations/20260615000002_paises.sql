-- Tabla de países (ISO 3166-1 alpha-2). Bandera = emoji derivado del código en la app.
CREATE TABLE IF NOT EXISTS public.paises (
  codigo text PRIMARY KEY,          -- ISO alpha-2: 'AR','US',...
  nombre text NOT NULL,
  activo boolean NOT NULL DEFAULT true
);
ALTER TABLE public.paises ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "paises: select" ON public.paises;
CREATE POLICY "paises: select" ON public.paises FOR SELECT TO authenticated USING (true);

-- FK país en documentos_tipos (default AR para los existentes)
ALTER TABLE public.documentos_tipos
  ADD COLUMN IF NOT EXISTS pais_id text REFERENCES public.paises(codigo);

