-- ============================================================
-- Normalización localidad/provincia → tabla localidades + FK
-- ============================================================

-- Tabla de referencia
CREATE TABLE localidades (
  id         uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  nombre     text        NOT NULL,
  provincia  text        NOT NULL,
  is_active  boolean     NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT uq_localidad_provincia UNIQUE (nombre, provincia)
);

CREATE INDEX idx_localidades_provincia ON localidades(provincia);

ALTER TABLE localidades ENABLE ROW LEVEL SECURITY;
CREATE POLICY "localidades_select_auth" ON localidades FOR SELECT TO authenticated USING (true);
CREATE POLICY "localidades_write_dev"   ON localidades FOR ALL   TO authenticated
  USING  ((SELECT system_role FROM profiles WHERE id = auth.uid()) = 'developer')
  WITH CHECK ((SELECT system_role FROM profiles WHERE id = auth.uid()) = 'developer');

