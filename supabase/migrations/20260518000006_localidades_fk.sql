-- ── FK en empresas ────────────────────────────────────────────

ALTER TABLE empresas
  ADD COLUMN localidad_id uuid REFERENCES localidades(id) ON DELETE SET NULL;

UPDATE empresas e
SET localidad_id = l.id
FROM localidades l
WHERE lower(trim(e.localidad)) = lower(trim(l.nombre))
  AND lower(trim(e.provincia)) = lower(trim(l.provincia))
  AND e.localidad IS NOT NULL;

ALTER TABLE empresas DROP COLUMN localidad;
ALTER TABLE empresas DROP COLUMN provincia;

CREATE INDEX IF NOT EXISTS idx_emp_localidad
  ON empresas(localidad_id) WHERE localidad_id IS NOT NULL;

